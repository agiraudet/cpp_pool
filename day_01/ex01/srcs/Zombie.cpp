#include "Zombie.hpp"

Zombie::Zombie(void)
{
	std::cout << "A new zombie rises from his grave..." << std::endl;
	return ;
}

Zombie::~Zombie(void)
{
	std::cout << this->_name << " got back underground..." << std::endl;
	return ;
}

void	Zombie::setName(std::string name)
{
	this->_name = name;
}

void	Zombie::announce(void) const
{
	std::cout << this->_name << ": BraiiiiiiinnnzzzZ..." << std::endl;
}
